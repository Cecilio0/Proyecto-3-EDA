package problemaJarras;

public class Jarra {
	
	private int size;
	private int cantAgua;
	
	public Jarra(int size) {
		this.size = size;
		cantAgua = 0;
	}
	
	public int getSize() {
		return size;
	}

	public int getCantAgua() {
		return cantAgua;
	}

	public void setCantAgua(int cantAgua) {
		this.cantAgua = cantAgua;
	}
	
	

}
